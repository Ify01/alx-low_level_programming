#include <stdio.h>

/**
 * add - adding
 * @a: value
 * @b: value
 * Return: success or error
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substracting
 * @a: value
 * @b: value
 * Return: success or error
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - dividing
 * @a: value
 * @b: value
 * Return: success or error
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiplication
 * @a: value
 * @b: value
 * Return: success or error
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - remaining
 * @a: value
 * @b: value
 * Return: success or error
 */

int mod(int a, int b)
{
	return (a % b);
}
