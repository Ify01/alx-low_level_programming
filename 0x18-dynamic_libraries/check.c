#include <stdio.h>

/**
 * add - adding
 * @a: value
 * @b: value
 * Return: success or error
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * minus - substracting
 * @a: value
 * @b: value
 * Return: success or error
 */

int minus(int a, int b)
{
	return (a - b);
}

/**
 * divide - adding
 * @a: value
 * @b: value
 * Return: success or error
 */

int divide(int a, int b)
{
	return (a / b);
}

/**
 * times - multiplication
 * @a: value
 * @b: value
 * Return: success or error
 */

int times(int a, int b)
{
	return (a * b);
}

/**
 * remain - remaining
 * @a: value
 * @b: value
 * Return: success or error
 */

int remain(int a, int b)
{
	return (a % b);
}
